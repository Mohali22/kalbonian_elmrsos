# Memory management across languages #
# ---------------------------------- #

# A computer uses two main types of storage.
# 1- hard disk (HD) There's the drive, which stores programs and data even when the computer is shut down.

# 2- Random-access memory (RAM) is the memory that holds a copy of any code that's running,
# as well as the results of any calculations and other manipulation that my programs do.


# Garbage Collection #
# ----------------------------- #

# This is an automated process in which the compiler keeps track of which items in memory are no longer needed and deletes them.


# Multithreading #
# -------------- #

# Multithreading specifically refers to the concurrent execution of more than one sequential set (thread) of instructions.
# Multithreaded programming is programming multiple, concurrent execution threads.
# These threads could run on a single processor.
# Or there could be multiple threads running on multiple processor cores


# algorithm #
# -------------- #

# A programming algorithm is a procedure or formula used for solving a problem.
# It is based on conducting a sequence of specified actions in which these actions describe how to do something,
# and your computer will do it exactly that way every time. An algorithm works by following a procedure,
# made up of inputs. Once it has followed all the inputs, it will see a result, also known as output.
